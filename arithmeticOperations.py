# realizando operações aritméticas
arithmeticOperations = 5 + 2 # soma
print("5 + 2 =", arithmeticOperations)

arithmeticOperations = 5 - 2 # subtração
print("5 - 2 =", arithmeticOperations)

arithmeticOperations = 5 * 2 # multiplicação
print("5 * 2 =", arithmeticOperations)

arithmeticOperations = 5 ** 2 # multiplicação ao quadrado
print("5 * 2 =", arithmeticOperations)

arithmeticOperations = 5 / 2 # divisão
print("5 / 2 =", arithmeticOperations)

arithmeticOperations = 5 // 2 # divisão com resultado inteiro
print("5 // 2 =", arithmeticOperations)

arithmeticOperations = 5 % 2 # resto da divisão
print("5 % 2 =", arithmeticOperations)
